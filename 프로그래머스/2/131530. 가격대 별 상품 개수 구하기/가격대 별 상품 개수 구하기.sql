# SELECT CASE
#             WHEN PRICE < 10000 THEN 0
#             WHEN PRICE >= 10000 THEN 10000
#             WHEN PRICE >= 20000 THEN 20000
#             WHEN PRICE >= 30000 THEN 30000
#         END AS PRICE_GROUP,
#         COUNT(*) AS PRODUCTS
# FROM PRODUCT
# GROUP BY PRICE_GROUP
# ORDER BY PRICE_GROUP
SELECT 
    FLOOR(PRICE / 10000) * 10000 AS PRICE_GROUP,
    COUNT(*) AS PRODUCTS
FROM 
    PRODUCT
WHERE 
    PRICE >= 10000
GROUP BY 
    PRICE_GROUP
ORDER BY 
    PRICE_GROUP;
